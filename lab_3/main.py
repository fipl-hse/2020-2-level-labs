class NGramTrie:

    def __init__(self, n: int):
        self.size = n
        self.n_grams = ()
        self.n_gram_frequencies = {}
        self.n_gram_log_probabilities = {}

    def fill_n_grams(self, encoded_text: tuple) -> int:
        """
        Extracts n-grams from the given sentence, fills the field n_grams
        :return: 0 if succeeds, 1 if not
        """
        if not isinstance(encoded_text, tuple):
            return 1
        self.n_grams = []
        for sentence in encoded_text:
            sentences = []
            for word in sentence:
                words = []
                for index in range(len(word) - self.size + 1):

                    words.append(tuple(word[index:index + self.size]))

                sentences.append(tuple(words))

            self.n_grams.append(tuple(sentences))

        self.n_grams = tuple(self.n_grams)
        return 0



    def calculate_n_grams_frequencies(self) -> int:
        """
        Fills in the n-gram storage from a sentence, fills the field n_gram_frequencies
        :return: 0 if succeeds, 1 if not
        """
        if len(self.n_grams) == 0:
            return 1
        for sentence in self.n_grams:
            for word in sentence:
                for n_gram in word:
                    self.n_gram_frequencies[n_gram] = self.n_gram_frequencies.get(n_gram, 0) + 1
        return 0

    def calculate_log_probabilities(self) -> int:
        """
        Gets log-probabilities of n-grams, fills the field n_gram_log_probabilities
        :return: 0 if succeeds, 1 if not
        """
        if not self.n_gram_frequencies:
            return 1
        for n_gram1 in self.n_gram_frequencies:
            den = 0

            for n_gram2 in self.n_gram_frequencies:
                if n_gram1[0] == n_gram2[0]:
                    den += self.n_gram_frequencies[n_gram2]

            if den == 0:
                den = 1

            probability = self.n_gram_frequencies[n_gram1] / den
            self.n_gram_log_probabilities[n_gram1] = log(probability)
        return 0

    def top_n_grams(self, k: int) -> tuple:
        """
        Gets k most common n-grams
        :return: a tuple with k most common n-grams
        """
        if not isinstance(k, int):
            return ()
        top_n_grams = sorted(self.n_gram_frequencies, key=self.n_gram_frequencies.get, reverse=True)
        return tuple(top_n_grams[:k])


